    private void bRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistrarActionPerformed
        File archivo = new File("C:\\Users\\lujah\\OneDrive\\Escritorio\\VoltGym\\Instructores.txt");
        
        try(FileWriter fichero = new FileWriter(archivo, true))
        {
            PrintWriter escritor = new PrintWriter(fichero);
            
            escritor.println(txtClave.getText());
            escritor.println(txtNombre.getText());
            escritor.println(txtApellidoP.getText());
            escritor.println(txtApellidoM.getText());
            escritor.println(txtTel.getText());
            JOptionPane.showMessageDialog(null, "Instructor Guardado con Exito!");
            
            txtClave.setText("");
            txtNombre.setText("");
            txtApellidoP.setText("");
            txtApellidoM.setText("");
            txtTel.setText("");
            
            txtClave.requestFocus();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
            
    }//GEN-LAST:event_bRegistrarActionPerformed

